    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        // Cambiar Pestaña:
        pkgModelo.clsMetodo metodo = clsMetodo.getInstance();

        if (metodo.CheckEditar() == true) {
            tbpEditar.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(null, "Campo Vacio");
            tbpEditar.setSelectedIndex(0);
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

    private void btnEditarFutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarFutActionPerformed
        // Agregar Editar Futbolista:
        pkgModelo.clsMetodo metodo = clsMetodo.getInstance();

        if (metodo.CheckEditarFutbolista() == true) {
            frmPrincipal principal = frmPrincipal.getInstance();

            for (clsSeleccionFutbol persona : frmPrincipal.SeleccionFutbol) {
                if (persona.getId().equals(txtId.getText())) {

                    persona.setId(txtId.getText());
                    persona.setNombre(txtNombre.getText());
                    persona.setApellido(txtApellido.getText());
                    persona.setEdad(jcFecha.getDate());
                    ((clsFutbolista) persona).setDorsal(txtDorsal.getText());
                    ((clsFutbolista) persona).setDemarcacion(txtDemarcion.getText());
                }
                this.setVisible(false);
                principal.setVisible(true);
                metodo.Limpiar();
            }
        }
    }//GEN-LAST:event_btnEditarFutActionPerformed

    private void btnEditarEntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarEntActionPerformed
        // Agregar Editar Entrenador:
        pkgModelo.clsMetodo metodo = clsMetodo.getInstance();

        if (metodo.CheckEditarEntrenador() == true) {
            frmPrincipal principal = frmPrincipal.getInstance();

            for (clsSeleccionFutbol persona : frmPrincipal.SeleccionFutbol) {
                if (persona.getId().equals(txtId.getText())) {

                    persona.setId(txtId.getText());
                    persona.setNombre(txtNombre.getText());
                    persona.setApellido(txtApellido.getText());
                    persona.setEdad(jcFecha.getDate());
                    ((clsEntrenador) persona).setIdFederacion(txtFederacion.getText());
                }
                this.setVisible(false);
                principal.setVisible(true);
                metodo.Limpiar();
            }
        }
    }//GEN-LAST:event_btnEditarEntActionPerformed

