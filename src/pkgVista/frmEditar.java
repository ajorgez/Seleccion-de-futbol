    private void mnOpBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnOpBuscarActionPerformed
        // Buscar:
        pkgVista.frmBuscar buscar = frmBuscar.getInstance();
        pkgModelo.clsMetodo metodo = clsMetodo.getInstance();
        if (metodo.Array() == true) {
            buscar.lstCoincidencia.setModel(metodo.Listar());
            buscar.setTitle("Buscar");
            buscar.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "No hay Ningun Registro en la Selecion de Futbol");
        }
    }//GEN-LAST:event_mnOpBuscarActionPerformed

